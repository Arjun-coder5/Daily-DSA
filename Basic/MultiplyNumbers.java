
// This is a simple Java program that defines a method to multiply two numbers.
public class MultiplyNumbers {
    public static int multiplication(int a, int b) {
        return a * b;
    }

    // You can use this main method to test your function locally
    public static void main(String[] args) {
        System.out.println(multiplication(1, 2)); // Output: 2
        System.out.println(multiplication(8, 7)); // Output: 56
    }
}
